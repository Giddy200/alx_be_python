# Prompt the user for their current age and store the input as an integer
current_age = int(input("How old are you? "))
# Calculate the age in the year 2050.
# Assuming the current year is 2023, the difference is 27 years.
future_age = current_age + 27
# Print the calculated age in the specified format
print(f"In 2050, you will be {future_age} years old.")